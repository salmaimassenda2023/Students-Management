// lib/prisma.js
import { PrismaClient } from '@/app/generated/prisma'; // Adjust path if needed

let prisma;

if (process.env.NODE_ENV === 'production') {
    prisma = new PrismaClient();
} else {
    if (!global.prisma) {
        global.prisma = new PrismaClient();
    }
    prisma = global.prisma;
}

export default prisma;